print(f"Mean Squared Error (MSE): {mse_knn:.2f}")
print(f"Root Mean Squared Error (RMSE): {rmse_knn:.2f}")
print(f"R-squared (R2 ): {r2_knn:.2f}")

"""### Support Vector Machine (SVM) Regression"""

